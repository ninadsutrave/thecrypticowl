-- ─── VIEWS ───────────────────────────────────────────────────────────────────

-- Aggregate reaction counts per clue (no user_id exposed)
CREATE OR REPLACE VIEW clue_reaction_counts AS
SELECT
  clue_id,
  SUM(reaction = 'like')    AS likes,
  SUM(reaction = 'dislike') AS dislikes,
  COUNT(*)                   AS total
FROM clue_reactions
GROUP BY clue_id;


-- Per-clue solve analytics for clue authors / admin dashboard.
-- MySQL 8 does not support FILTER (WHERE ...) in aggregates — use CASE WHEN.
CREATE OR REPLACE VIEW clue_solve_stats AS
SELECT
  c.id                                                                             AS clue_id,
  dp.date,
  c.primary_type,
  c.difficulty,
  c.author,

  COUNT(DISTINCT sh.user_id)                                                       AS total_solves,

  -- Hint funnel
  SUM(CASE WHEN sh.hints_used >= 1 THEN 1 ELSE 0 END)                            AS hint_1_opens,
  SUM(CASE WHEN sh.hints_used >= 2 THEN 1 ELSE 0 END)                            AS hint_2_opens,
  SUM(CASE WHEN sh.hints_used >= 3 THEN 1 ELSE 0 END)                            AS hint_3_opens,
  SUM(CASE WHEN sh.hints_used >= 4 THEN 1 ELSE 0 END)                            AS hint_4_opens,

  -- Difficulty signals
  ROUND(AVG(sh.hints_used),            2)                                          AS avg_hints_used,
  ROUND(AVG(sh.solve_time_seconds),    0)                                          AS avg_solve_seconds,
  ROUND(AVG(sh.wrong_attempts),        2)                                          AS avg_wrong_attempts,
  SUM(CASE WHEN sh.hints_used = 0 THEN 1 ELSE 0 END)                             AS zero_hint_solves,

  -- Quality signals
  COALESCE(rc.likes,    0)                                                          AS likes,
  COALESCE(rc.dislikes, 0)                                                          AS dislikes,
  CASE
    WHEN COALESCE(rc.total, 0) = 0 THEN NULL
    ELSE ROUND(rc.likes * 100.0 / rc.total, 1)
  END                                                                               AS like_pct

FROM clues c
LEFT JOIN daily_puzzles        dp ON dp.clue_id = c.id
LEFT JOIN solve_history        sh ON sh.clue_id = c.id
LEFT JOIN clue_reaction_counts rc ON rc.clue_id = c.id
GROUP BY
  c.id, dp.date, c.primary_type, c.difficulty, c.author,
  rc.likes, rc.dislikes, rc.total;


