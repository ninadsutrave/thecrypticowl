-- =============================================================================
-- CrypticOwl — MySQL 8.0+ Schema
-- Equivalent of 001_initial.sql (PostgreSQL) adapted for MySQL 8.0.
--
-- Key differences vs PostgreSQL version:
--   • No built-in auth.users — includes a `users` table for identity
--   • No Row Level Security (RLS) — enforce access in application layer / API
--   • UUID()  replaces gen_random_uuid()
--   • JSON    replaces JSONB
--   • No partial indexes (WHERE clause) — use standard indexes instead
--   • No FILTER clause in aggregates — use CASE WHEN instead
--   • ON DUPLICATE KEY UPDATE replaces ON CONFLICT ... DO UPDATE
--   • INSERT IGNORE            replaces ON CONFLICT ... DO NOTHING
--   • STORED generated columns use MySQL syntax
--   • DELIMITER $$ for stored procedures / functions
--   • TEXT[] (PostgreSQL arrays) stored as JSON arrays
--   • TIMESTAMPTZ → DATETIME(3) (UTC, stored without TZ offset; store all UTC)
--   • GENERATED BY DEFAULT AS IDENTITY → AUTO_INCREMENT
-- =============================================================================

