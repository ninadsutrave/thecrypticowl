-- ─── CLUES ───────────────────────────────────────────────────────────────────

CREATE TABLE IF NOT EXISTS clues (
  id               CHAR(36)       NOT NULL PRIMARY KEY DEFAULT (UUID()),

  -- clue_text is UNIQUE so automated inserts can use INSERT IGNORE
  clue_text        TEXT           NOT NULL,
  -- MySQL TEXT columns can't have a UNIQUE constraint directly — use a hash index
  -- via a generated column for exact de-duplication
  clue_text_hash   CHAR(64)       GENERATED ALWAYS AS (SHA2(clue_text, 256)) STORED NOT NULL,
  UNIQUE KEY uq_clue_text_hash (clue_text_hash),

  -- answer: uppercase letters only, no spaces/hyphens
  answer           VARCHAR(50)    NOT NULL,
  CONSTRAINT chk_answer_upper CHECK (answer = UPPER(answer)),
  CONSTRAINT chk_answer_alpha  CHECK (answer REGEXP '^[A-Z]+$'),

  -- Derived: total letter count. MySQL generated column, always correct.
  answer_length    TINYINT UNSIGNED GENERATED ALWAYS AS (CHAR_LENGTH(answer)) STORED NOT NULL,

  -- Display letter-count pattern: "5", "3,4", "2-3"
  answer_pattern   VARCHAR(20)    NOT NULL,
  CONSTRAINT chk_answer_pattern CHECK (answer_pattern REGEXP '^[0-9][0-9,\\-]*$'),

  -- Prevents same answer with same display pattern appearing twice
  UNIQUE KEY uq_answer_pattern (answer(50), answer_pattern),

  -- Wordplay metadata
  primary_type     VARCHAR(50)    NOT NULL,
  CONSTRAINT fk_clues_type FOREIGN KEY (primary_type) REFERENCES clue_wordplay_types(id),

  definition_text  TEXT           NOT NULL,
  wordplay_summary TEXT           NOT NULL,

  -- Frontend display data: JSON arrays (validates as JSON in MySQL 8+)
  clue_parts       JSON           NOT NULL DEFAULT (JSON_ARRAY()),
  hints            JSON           NOT NULL DEFAULT (JSON_ARRAY()),
  CONSTRAINT chk_clue_parts_array CHECK (JSON_TYPE(clue_parts) = 'ARRAY'),
  CONSTRAINT chk_hints_array      CHECK (JSON_TYPE(hints)      = 'ARRAY'),

  -- Tags stored as JSON array (e.g. ["british-english", "easy"])
  -- In PostgreSQL this is TEXT[]; in MySQL we use JSON
  tags             JSON           NOT NULL DEFAULT (JSON_ARRAY()),

  difficulty       ENUM('easy','medium','hard','expert') NOT NULL DEFAULT 'medium',
  author           VARCHAR(255),
  notes            TEXT,  -- internal only, never sent to the frontend

  created_at       DATETIME(3)    NOT NULL DEFAULT (NOW(3)),
  updated_at       DATETIME(3)    NOT NULL DEFAULT (NOW(3)),

  INDEX idx_clues_type       (primary_type),
  INDEX idx_clues_difficulty (difficulty)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;


-- ─── DAILY PUZZLES ───────────────────────────────────────────────────────────

CREATE TABLE IF NOT EXISTS daily_puzzles (
  id              CHAR(36)   NOT NULL PRIMARY KEY DEFAULT (UUID()),
  date            DATE       NOT NULL,
  clue_id         CHAR(36)   NOT NULL,
  CONSTRAINT fk_dp_clue FOREIGN KEY (clue_id) REFERENCES clues(id) ON DELETE CASCADE,

  published       TINYINT(1) NOT NULL DEFAULT 0,

  -- 1-based position for ordered multi-clue days; always 1 for current single-clue format
  sequence_number SMALLINT   NOT NULL DEFAULT 1,

  -- Sequential puzzle number shown to users ("Puzzle #42")
  -- AUTO_INCREMENT on a non-PK column requires it to be a key
  puzzle_number   INT        NOT NULL AUTO_INCREMENT,
  KEY key_puzzle_number (puzzle_number),  -- required for AUTO_INCREMENT on non-PK
  UNIQUE KEY uq_puzzle_number  (puzzle_number),
  UNIQUE KEY uq_date_clue      (date, clue_id),
  UNIQUE KEY uq_date_sequence  (date, sequence_number),

  INDEX idx_dp_date_published (date, published),
  INDEX idx_dp_clue_published (clue_id, published)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- Note: AUTO_INCREMENT on a non-PK column is only supported by InnoDB when the column
-- is the leftmost column of some key. The KEY definition above satisfies this.
-- To seed a specific starting number (e.g. puzzle_number = 42), run:
--   ALTER TABLE daily_puzzles AUTO_INCREMENT = 42;
-- before the first INSERT. Or set puzzle_number explicitly in the INSERT.


