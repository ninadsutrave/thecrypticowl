-- ─── STORED PROCEDURE: record_solve ──────────────────────────────────────────
-- Atomically inserts a solve record and updates user_stats.
-- MySQL does not support functions returning BOOLEAN from DML directly;
-- use a procedure with an OUT parameter instead.
--
-- Call from application layer:
--   CALL record_solve(p_user_id, p_clue_id, p_puzzle_number,
--                     p_hints_used, p_wrong_attempts, p_xp_earned,
--                     p_solve_time_seconds, p_client_date, @result);
--   SELECT @result;  -- 1 = new solve, 0 = already solved
--
-- p_client_date: the user's local date as 'YYYY-MM-DD'.
--   Pass NULL to fall back to CURDATE() (UTC).
--   Non-null value prevents streak breaks for users in timezones ahead of UTC.

CREATE PROCEDURE record_solve(
  IN  p_user_id             CHAR(36),
  IN  p_clue_id             CHAR(36),
  IN  p_puzzle_number       INT,
  IN  p_hints_used          SMALLINT,
  IN  p_wrong_attempts      INT,
  IN  p_xp_earned           INT,
  IN  p_solve_time_seconds  INT,      -- NULL if not tracked
  IN  p_client_date         DATE,     -- NULL to use CURDATE()
  OUT p_result              TINYINT   -- 1 = inserted, 0 = already existed
)
SQL SECURITY DEFINER
BEGIN
  DECLARE v_today    DATE;
  DECLARE v_last     DATE;
  DECLARE v_streak   INT;
  DECLARE v_best     INT;

  SET v_today = COALESCE(p_client_date, CURDATE());

  -- Insert solve record; skip silently if already solved (unique constraint)
  INSERT IGNORE INTO solve_history (
    id, user_id, clue_id, puzzle_number,
    hints_used, wrong_attempts, xp_earned, solve_time_seconds
  ) VALUES (
    UUID(), p_user_id, p_clue_id, p_puzzle_number,
    p_hints_used, p_wrong_attempts, p_xp_earned, p_solve_time_seconds
  );

  -- ROW_COUNT() = 1 means the INSERT happened; 0 means duplicate was skipped
  IF ROW_COUNT() = 0 THEN
    SET p_result = 0;
  ELSE
    -- Ensure user_stats row exists
    INSERT IGNORE INTO user_stats (user_id) VALUES (p_user_id);

    -- Read current streak state
    SELECT last_solved, streak_count, best_streak
    INTO   v_last, v_streak, v_best
    FROM   user_stats
    WHERE  user_id = p_user_id;

    -- Recalculate streak (NULL-safe: new users have NULL last_solved)
    SET v_streak = CASE
      WHEN v_last IS NULL               THEN 1              -- first solve ever
      WHEN v_last = DATE_SUB(v_today, INTERVAL 1 DAY)
                                        THEN v_streak + 1  -- consecutive day
      WHEN v_last = v_today             THEN v_streak       -- same day (idempotency edge)
      ELSE                                   1              -- streak broken
    END;

    SET v_best = GREATEST(v_best, v_streak);

    -- Atomic stats update
    UPDATE user_stats SET
      streak_count = v_streak,
      best_streak  = v_best,
      last_solved  = v_today,
      total_solved = total_solved + 1,
      xp           = xp + p_xp_earned
      -- level is computed client-side; omitted here to avoid duplicating formula
    WHERE user_id = p_user_id;

    SET p_result = 1;
  END IF;
END$$

